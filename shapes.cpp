#include <iostream>
using namespace std;
int main(){
	// ROWS
	for(int i = 0 ; i<5 ; i++){
		// COLUMNS
		for(int m = 0; m<=i; m++){
			cout<< "*";
		}
		cout<<endl;
	}
}


